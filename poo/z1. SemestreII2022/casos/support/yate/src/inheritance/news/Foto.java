package inheritance.news;

public class Foto {
	private int parrafo;
	private String url;
	
	public int getParrafo() {
		return parrafo;
	}
	
	public void setParrafo(int parrafo) {
		this.parrafo = parrafo;
	}
	
	public String getUrl() {
		return url;
	}
	
	public void setUrl(String url) {
		this.url = url;
	}
}
